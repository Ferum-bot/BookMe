package com.levit.book_me.roundcloudsview.core.enums

/**
 * This enum class provides current cloud state.
 * State changes after user's click on cloud.
 */
enum class RoundCloudState {

    /**
     * This state means that cloud have
     * @CHECKED background color and
     * @CHECKED text color.
     */
    CHECKED,

    /**
     *  This state means that cloud have
     *  @NOT_CHECKED background color and
     *  @NOT_CHECKED text color.
     */
    NOT_CHECKED
}