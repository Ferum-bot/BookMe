package com.levit.bookme.chatkit.models.enums

enum class MessageDateFormat {

    /**
     * Shows only time, for example 11:24.
     */
    SHOW_ONLY_TIME,

    /**
     * Shows only date, for example 11.02.2002.
     */
    SHOW_ONLY_DATE,

    /**
     * Shows all information, for example 11.02.2002 11:24
     */
    SHOW_ALL;
}